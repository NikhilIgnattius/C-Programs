#include <stdio.h>

int main() {
    int a=31,b=23;
    printf("\nBefore Incrementing:a=%d",a);
    a++;
    printf("\nAfter Incrementing :a=%d",a);
    
    printf("\nBefore Decrementing:b=%d",b);
    b--;
    printf("\nAfter Decrementing :b=%d",b);
    
    getchar();
    
}
